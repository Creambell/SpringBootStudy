package three.src.com.kh.operator;

public class F_Compound {
	public void method() {
		int num = 12;
		
		num += 3;
		System.out.println(num);
		num -= 5;
		System.out.println(num);
		num *= 6;
		System.out.println(num);
		num /= 2;
		System.out.println(num);
		num %= 4;
		System.out.println(num);
	}
}
